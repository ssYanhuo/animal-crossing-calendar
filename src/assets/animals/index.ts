  static groupBySpecie(version?: number){
    const result = new Map<string, any[]>();
    for (const animal of animalsACNH) {
      if (!result.has(animal.specie)) {
        result.set(animal.specie, []);
      }
      result.get(animal.specie)!.push(animal);
    }
    return result;
  }
  static getByName(name: string, version?: number){
    for (const animal of animalsACNH) {
      if (animal.name === name) {
        return animal;
      }
    }
  }
